package net.huimin.yk.web.model.system;

public class SysMenu {
    /**
     * 主键
     */
    private Integer id;

    /**
     * 菜单名称
     */
    private String menuName;

    /**
     * 菜单地址
     */
    private String menuUrl;

    /**
     * 排序号
     */
    private Integer sortDecimal;

    /**
     * @return 主键
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id 
	 *            主键
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return 菜单名称
     */
    public String getMenuName() {
        return menuName;
    }

    /**
     * @param menuName 
	 *            菜单名称
     */
    public void setMenuName(String menuName) {
        this.menuName = menuName;
    }

    /**
     * @return 菜单地址
     */
    public String getMenuUrl() {
        return menuUrl;
    }

    /**
     * @param menuUrl 
	 *            菜单地址
     */
    public void setMenuUrl(String menuUrl) {
        this.menuUrl = menuUrl;
    }

    /**
     * @return 排序号
     */
    public Integer getSortDecimal() {
        return sortDecimal;
    }

    /**
     * @param sortDecimal 
	 *            排序号
     */
    public void setSortDecimal(Integer sortDecimal) {
        this.sortDecimal = sortDecimal;
    }
}